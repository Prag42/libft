#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*dup;
	int		i;
	int		msize;

	msize = 0;
	i = 0;
	while (str[msize])
		msize++;
	dup = malloc(sizeof(char) * (msize + 1));
	if (dup == NULL)
		return (NULL);
	while (str[i])
	{
		dup[i] = str[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
